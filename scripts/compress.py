#!/usr/bin/env python3
"""
geojson_to_arduino.py
=====================
Converts timezones_dst.geojson → two Arduino header files:

  timezone_data.h      — timezone data in PROGMEM (Flash)
  timezone_lookup.h    — coordinate lookup algorithm

Usage:
    python geojson_to_arduino.py [--input timezones_dst.geojson]
                                 [--out-data timezone_data.h]
                                 [--out-lookup timezone_lookup.h]
                                 [--max-pts 120]

Platform Flash limits:
    Arduino UNO/Nano : ~32 KB  — only feasible with heavily simplified zones
    Arduino Mega     : ~256 KB — sufficient for most zones
    ESP8266          : ~1 MB   — comfortable
    ESP32            : ~4 MB   — no practical limit

Coordinates are stored as int16 (× 100), giving ~1.1 km precision at the equator.
UTC offsets are stored as int16 (× 100) to cover fractional zones
such as India (+5:30 → 550) and Nepal (+5:45 → 575).
"""

import argparse
import json
import sys
from pathlib import Path


# ─────────────────────────────────────────────────────────────────
# Helper functions
# ─────────────────────────────────────────────────────────────────

def flatten_rings(geometry: dict) -> list[list[tuple[float, float]]]:
    """Extracts all outer rings from a Polygon or MultiPolygon."""
    rings = []
    gtype = geometry.get("type", "")
    coords = geometry.get("coordinates", [])
    if gtype == "Polygon":
        if coords:
            rings.append(coords[0])           # outer ring only
    elif gtype == "MultiPolygon":
        for poly in coords:
            if poly:
                rings.append(poly[0])
    return rings


def bbox_of_ring(ring) -> tuple[float, float, float, float]:
    """(lon_min, lat_min, lon_max, lat_max)"""
    lons = [p[0] for p in ring]
    lats = [p[1] for p in ring]
    return min(lons), min(lats), max(lons), max(lats)


def offset_to_int(h: float) -> int:
    """UTC offset in hours → int16 × 100 (covers ±14 h and fractional zones)."""
    return int(round(h * 100))


def parse_posix_rule(rule: str | None) -> tuple[int, int, int, int, int]:
    """
    Parses a POSIX rule Mm.w.d/hh:mm → (month, week, dow, hour, minute).
    Returns (0,0,0,0,0) if rule is None or unrecognised.
    """
    if not rule:
        return (0, 0, 0, 0, 0)
    try:
        # format: M3.5.0/02:00
        m_part, time_part = rule.split("/")
        m_part = m_part.lstrip("M")
        month, week, dow = (int(x) for x in m_part.split("."))
        if ":" in time_part:
            hh, mm = (int(x) for x in time_part.split(":"))
        else:
            hh, mm = int(time_part), 0
        return (month, week, dow, hh, mm)
    except Exception:
        return (0, 0, 0, 0, 0)


def c_str(s: str, max_len: int) -> str:
    """Truncates a string and escapes it for a C string literal."""
    s = s[:max_len - 1].replace('"', '\\"').replace('\\', '\\\\')
    return s


def chunks(lst, n):
    for i in range(0, len(lst), n):
        yield lst[i:i + n]


# ─────────────────────────────────────────────────────────────────
# Generate timezone_data.h
# ─────────────────────────────────────────────────────────────────

RING_SEP = (32767, 32767)   # sentinel value separating rings within one zone

def build_data_header(features: list, max_pts: int) -> str:
    lines = []
    lines.append("// AUTO-GENERATED by geojson_to_arduino.py — DO NOT EDIT")
    lines.append("// Timezone boundary data in PROGMEM (Flash)")
    lines.append("//")
    lines.append("// Point encoding: int16, value = coordinate × 100")
    lines.append("// UTC offset encoding: int16, value = hours × 100")
    lines.append("//   e.g. India +5:30 → 550, Nepal +5:45 → 575")
    lines.append("// DST rule encoding: uint8[5] = {month, week, dow, hour, minute}")
    lines.append("//   POSIX Mm.w.d/hh:mm, week=5 means 'last'")
    lines.append("#pragma once")
    lines.append("#include <stdint.h>")
    lines.append("")
    lines.append("// Platform detection: AVR (UNO/Mega), ESP32/ESP8266, or desktop")
    lines.append("#if defined(ARDUINO)")
    lines.append("#  if defined(__AVR__)")
    lines.append("#    include <avr/pgmspace.h>")
    lines.append("#  elif defined(ESP8266) || defined(ESP32)")
    lines.append("#    include <pgmspace.h>")
    lines.append("#  else")
    lines.append("//   Other Arduino-compatible boards (RP2040, STM32, SAMD, Teensy...)")
    lines.append("//   pgmspace.h is usually not needed; PROGMEM is a no-op")
    lines.append("#    ifndef PROGMEM")
    lines.append("#      define PROGMEM")
    lines.append("#    endif")
    lines.append("#    ifndef pgm_read_byte")
    lines.append("#      define pgm_read_byte(addr)   (*(const uint8_t*)(addr))")
    lines.append("#    endif")
    lines.append("#    ifndef pgm_read_word")
    lines.append("#      define pgm_read_word(addr)   (*(const uint16_t*)(addr))")
    lines.append("#    endif")
    lines.append("#    ifndef pgm_read_dword")
    lines.append("#      define pgm_read_dword(addr)  (*(const uint32_t*)(addr))")
    lines.append("#    endif")
    lines.append("#    ifndef memcpy_P")
    lines.append("#      define memcpy_P memcpy")
    lines.append("#    endif")
    lines.append("#  endif")
    lines.append("#else")
    lines.append("// Desktop / unit-test build")
    lines.append("#  ifndef PROGMEM")
    lines.append("#    define PROGMEM")
    lines.append("#  endif")
    lines.append("#  ifndef pgm_read_byte")
    lines.append("#    define pgm_read_byte(addr)   (*(const uint8_t*)(addr))")
    lines.append("#  endif")
    lines.append("#  ifndef pgm_read_word")
    lines.append("#    define pgm_read_word(addr)   (*(const uint16_t*)(addr))")
    lines.append("#  endif")
    lines.append("#  ifndef pgm_read_dword")
    lines.append("#    define pgm_read_dword(addr)  (*(const uint32_t*)(addr))")
    lines.append("#  endif")
    lines.append("#  ifndef memcpy_P")
    lines.append("#    include <string.h>")
    lines.append("#    define memcpy_P memcpy")
    lines.append("#  endif")
    lines.append("#endif")
    lines.append("#ifndef pgm_read_int16")
    lines.append("static inline int16_t pgm_read_int16(const void* a){return *(const int16_t*)(a);}")
    lines.append("#endif")
    lines.append("")

    all_points  = []   # all points in order, indexed into the global array
    zone_metas  = []   # per-zone metadata

    skipped = 0

    for feat in features:
        props   = feat.get("properties", {}) or {}
        geom    = feat.get("geometry")
        tzid    = props.get("tzid", "")
        country = props.get("country", "XX")

        if not geom or not tzid:
            continue

        rings = flatten_rings(geom)
        if not rings:
            continue

        # Global bounding box for the zone (union of all rings)
        all_lons, all_lats = [], []
        ring_data = []   # list of [(ix, iy)] per ring

        for ring in rings:
            pts = [(p[0], p[1]) for p in ring]
            if len(pts) < 3:
                continue
            # Uniform decimation to max_pts points
            if len(pts) > max_pts:
                step  = len(pts) / max_pts
                pts   = [pts[int(i * step)] for i in range(max_pts)]
            # Remove the duplicate closing point of a closed ring
            if pts and pts[0] == pts[-1]:
                pts = pts[:-1]
            if len(pts) < 3:
                continue

            ix_iy = [(int(round(x * 100)), int(round(y * 100))) for x, y in pts]
            all_lons.extend(x for x, _ in ix_iy)
            all_lats.extend(y for _, y in ix_iy)
            ring_data.append(ix_iy)

        if not ring_data:
            skipped += 1
            continue

        bbox = (min(all_lons), min(all_lats), max(all_lons), max(all_lats))

        # Write points into the global array; rings are separated by RING_SEP
        start_idx = len(all_points)
        for i, ring_pts in enumerate(ring_data):
            all_points.extend(ring_pts)
            if i < len(ring_data) - 1:
                all_points.append(RING_SEP)   # ring separator

        end_idx = len(all_points)

        # DST metadata
        has_dst       = bool(props.get("has_dst", False))
        utc_std       = offset_to_int(props.get("utc_offset_std", 0.0))
        utc_dst       = offset_to_int(props.get("utc_offset_dst", 0.0))
        dst_off       = offset_to_int(props.get("dst_offset_hours", 0.0))
        abbr_std      = c_str(props.get("abbr_std", ""), 10)
        abbr_dst      = c_str(props.get("abbr_dst", ""), 10)
        dst_start     = parse_posix_rule(props.get("dst_start"))
        dst_end       = parse_posix_rule(props.get("dst_end"))
        tzid_s        = c_str(tzid, 40)
        country_s     = c_str(country, 4)

        zone_metas.append({
            "tzid":       tzid_s,
            "country":    country_s,
            "bbox":       bbox,
            "start_idx":  start_idx,
            "end_idx":    end_idx,
            "ring_count": len(ring_data),
            "has_dst":    has_dst,
            "utc_std":    utc_std,
            "utc_dst":    utc_dst,
            "dst_off":    dst_off,
            "abbr_std":   abbr_std,
            "abbr_dst":   abbr_dst,
            "dst_start":  dst_start,
            "dst_end":    dst_end,
        })

    if skipped:
        print(f"  ⚠ Zones skipped (no geometry): {skipped}", file=sys.stderr)

    total_pts   = len(all_points)
    total_zones = len(zone_metas)
    data_kb     = (total_pts * 4 + total_zones * 90) / 1024

    print(f"  Zones:  {total_zones}")
    print(f"  Points: {total_pts}")
    print(f"  ~Flash: {data_kb:.1f} KB")

    # ── Global point array ───────────────────────────────────────
    lines.append(f"// Total zones: {total_zones},  total coordinate pairs: {total_pts}")
    lines.append(f"// Estimated Flash usage: ~{data_kb:.0f} KB")
    lines.append(f"#define TZ_ZONE_COUNT {total_zones}")
    lines.append(f"#define TZ_POINT_COUNT {total_pts}")
    lines.append("")

    # Point array: interleaved x, y as int16
    lines.append("// All polygon vertices: interleaved [lon×100, lat×100] pairs.")
    lines.append("// Rings within a zone are separated by sentinel (32767, 32767).")
    lines.append("static const int16_t TZ_POINTS[] PROGMEM = {")
    flat = []
    for ix, iy in all_points:
        flat.append(ix)
        flat.append(iy)
    for row in chunks(flat, 16):
        lines.append("  " + ", ".join(f"{v:6d}" for v in row) + ",")
    lines.append("};")
    lines.append("")

    # ── Zone struct ──────────────────────────────────────────────
    lines.append("typedef struct {")
    lines.append("  char           tzid[40];      // IANA timezone id")
    lines.append("  char           country[4];    // ISO 3166-1 alpha-2")
    lines.append("  int16_t        bbox[4];        // lon_min,lat_min,lon_max,lat_max (×100)")
    lines.append("  uint16_t       pt_start;       // index into TZ_POINTS (pair index)")
    lines.append("  uint16_t       pt_end;         // exclusive end")
    lines.append("  uint8_t        ring_count;     // number of rings")
    lines.append("  uint8_t        has_dst;")
    lines.append("  int16_t        utc_std;        // UTC offset standard × 100 (minutes*100/60)")
    lines.append("  int16_t        utc_dst;        // UTC offset DST × 100")
    lines.append("  int16_t        dst_offset;     // shift magnitude × 100")
    lines.append("  char           abbr_std[10];")
    lines.append("  char           abbr_dst[10];")
    lines.append("  uint8_t        dst_start[5];   // {month,week,dow,hour,min}")
    lines.append("  uint8_t        dst_end[5];     // {month,week,dow,hour,min}")
    lines.append("} TzZone;")
    lines.append("")

    # ── Zone array ───────────────────────────────────────────────
    lines.append("static const TzZone TZ_ZONES[] PROGMEM = {")
    for z in zone_metas:
        b = z["bbox"]
        ds = z["dst_start"]
        de = z["dst_end"]
        lines.append(f'  {{ "{z["tzid"]}", "{z["country"]}", '
                     f'{{{b[0]},{b[1]},{b[2]},{b[3]}}}, '
                     f'{z["start_idx"]}, {z["end_idx"]}, '
                     f'{z["ring_count"]}, '
                     f'{1 if z["has_dst"] else 0}, '
                     f'{z["utc_std"]}, {z["utc_dst"]}, {z["dst_off"]}, '
                     f'"{z["abbr_std"]}", "{z["abbr_dst"]}", '
                     f'{{{ds[0]},{ds[1]},{ds[2]},{ds[3]},{ds[4]}}}, '
                     f'{{{de[0]},{de[1]},{de[2]},{de[3]},{de[4]}}}  }},')
    lines.append("};")
    lines.append("")

    return "\n".join(lines)



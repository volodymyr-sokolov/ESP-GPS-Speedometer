def main():
    ap = argparse.ArgumentParser(
        description="Converts timezones_dst.geojson → Arduino .h files"
    )
    ap.add_argument("--input",      default="timezones_dst.geojson",
                    help="Input GeoJSON file")
    ap.add_argument("--out-data",   default="timezone_data.h",
                    help="Output data file")
    ap.add_argument("--out-lookup", default="timezone_lookup.h",
                    help="Output lookup algorithm file")
    ap.add_argument("--max-pts",    type=int, default=120,
                    help="Max points per ring (fewer → less Flash). "
                         "Recommendations: UNO=40, Mega=80, ESP8266=120, ESP32=200")
    args = ap.parse_args()

    inp = Path(args.input)
    if not inp.exists():
        print(f"❌ File not found: {inp}", file=sys.stderr)
        sys.exit(1)

    print(f"📖 Reading {inp} ...")
    with open(inp, encoding="utf-8") as f:
        data = json.load(f)

    features = data.get("features", [])
    print(f"   Features loaded: {len(features)}")

    # ── timezone_data.h ──
    print(f"⚙  Generating {args.out_data} (max {args.max_pts} points/ring) ...")
    data_h = build_data_header(features, args.max_pts)
    out_data = Path(args.out_data)
    out_data.write_text(data_h, encoding="utf-8")
    kb = out_data.stat().st_size / 1024
    print(f"   ✔ {out_data}  ({kb:.1f} KB)")

    # ── timezone_lookup.h ──
    out_lookup = Path(args.out_lookup)
    out_lookup.write_text(LOOKUP_H, encoding="utf-8")
    kb2 = out_lookup.stat().st_size / 1024
    print(f"   ✔ {out_lookup}  ({kb2:.1f} KB)")

    print()
    print("═" * 60)
    print("  ✅ Done!")
    print()
    print("  Include in your Arduino sketch:")
    print(f"    #include \"{out_data.name}\"")
    print(f"    #include \"{out_lookup.name}\"")
    print()
    print("  Example call:")
    print("    TzResult r = tz_lookup(52.52f, 13.405f,")
    print("                           2026, 6, 15, 10, 0);")
    print("    if (r.valid) {")
    print("      Serial.println(r.country);     // \"DE\"")
    print("      Serial.println(r.abbr);        // \"CEST\"")
